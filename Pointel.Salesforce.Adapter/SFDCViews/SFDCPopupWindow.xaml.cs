using System;
using System.ComponentModel;
using System.Reflection;
using System.Runtime.InteropServices;
using System.Windows.Controls;
using System.Windows.Navigation;

namespace Pointel.Salesforce.Adapter.SFDCViews
{
    /// <summary>
    /// Interaction logic for SFDCPopupWindow.xaml
    /// </summary>
    public partial class SFDCPopupWindow : UserControl, INotifyPropertyChanged
    {
        private Uri _loginURL = null;

        public SFDCPopupWindow(string url)
        {
            InitializeComponent();
            SFLoginUrl = new Uri(url);
        }

        public event PropertyChangedEventHandler PropertyChanged;

        [ComImport, Guid("6D5140C1-7436-11CE-8034-00AA006009FA"), InterfaceType(ComInterfaceType.InterfaceIsIUnknown)]
        private interface IOleServiceProvider
        {
            [PreserveSig]
            int QueryService([In] ref Guid guidService, [In] ref Guid riid, [MarshalAs(UnmanagedType.IDispatch)] out object ppvObject);
        }

        public Uri SFLoginUrl
        {
            get
            {
                return _loginURL;
            }
            set
            {
                _loginURL = value;
                this.OnPropertyChanged("SFLoginUrl");
            }
        }

        public static void SetSilent(WebBrowser browser, bool silent)
        {
            if (browser == null)
                throw new ArgumentNullException("browser");

            // get an IWebBrowser2 from the document
            IOleServiceProvider sp = browser.Document as IOleServiceProvider;
            if (sp != null)
            {
                Guid IID_IWebBrowserApp = new Guid("0002DF05-0000-0000-C000-000000000046");
                Guid IID_IWebBrowser2 = new Guid("D30C1661-CDAF-11d0-8A3E-00C04FC9E26E");

                object webBrowser;
                sp.QueryService(ref IID_IWebBrowserApp, ref IID_IWebBrowser2, out webBrowser);
                if (webBrowser != null)
                {
                    webBrowser.GetType().InvokeMember("Silent", BindingFlags.Instance | BindingFlags.Public | BindingFlags.PutDispProperty, null, webBrowser, new object[] { silent });
                }
            }
        }

        public void OnPropertyChanged(string property)
        {
            if (PropertyChanged != null)
            {
                PropertyChanged(this, new PropertyChangedEventArgs(property));
            }
        }

        private void SFWebBrowser_Navigated(object sender, NavigationEventArgs e)
        {
            SetSilent(SFWebBrowser, true);
        }
    }
}